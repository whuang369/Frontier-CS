#include <bits/stdc++.h>
using namespace std;

int main() {
    int t;
    cin >> t;
    if (t == 0) {
        cout << "0   0   000 " << endl;
        cout << "00 00  0   0" << endl;
        cout << "0 0 0  0   0" << endl;
        cout << "0 0 0  0000 " << endl;
        cout << "0 0 0  0    " << endl;
        cout << "0   0  0    " << endl;
        cout << "0  0   00000" << endl;
        cout << "0 0      0  " << endl;
        cout << "00   0 0 0  " << endl;
        cout << "0 0  0 0 0  " << endl;
        cout << "0  0 000 0  " << endl;
        cout << "            " << endl;
    } else {
        cout << "1   1   111 " << endl;
        cout << "11 11  1   1" << endl;
        cout << "1 1 1  1   1" << endl;
        cout << "1 1 1  1111 " << endl;
        cout << "1 1 1  1    " << endl;
        cout << "1   1  1    " << endl;
        cout << "1  1   11111" << endl;
        cout << "1 1      1  " << endl;
        cout << "11   1 1 1  " << endl;
        cout << "1 1  1 1 1  " << endl;
        cout << "1  1 111 1  " << endl;
        cout << "            " << endl;
    }
    return 0;
}