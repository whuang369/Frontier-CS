#include <bits/stdc++.h>
using namespace std;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    
    int N, R;
    while (cin >> N >> R) {
        for (int i = 0; i < N; ++i) {
            int U, V;
            cin >> U >> V;
        }
        string T;
        if (cin >> T) {
            cout << T << '\n';
        } else {
            break;
        }
    }
    return 0;
}