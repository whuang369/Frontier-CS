#include <bits/stdc++.h>
using namespace std;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    
    int sx, sy;
    if (!(cin >> sx >> sy)) return 0;

    for (int t = 1; t <= 3000; ++t) {
        cout << 1 << ' ' << 1 << '\n';
        cout.flush();

        int nx, ny;
        if (!(cin >> nx >> ny)) break;
        if (nx == 0 && ny == 0) break;
    }

    return 0;
}